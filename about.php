Philly-Chix is Philadelphia's chapter of <a href="http://www.linuxchix.org">LinuxChix</a>. All meetings are free and open to any woman interested.<br><br>

<b>About Philly-Chix and Why It Was Started</b><br><br>

We wanted to meet women who were interested in Linux (or any other *nix). Since we were members of the LinuxChix mailing lists, we found solace in talking about issues that we weren't entirely comfortable talking to men  about.  The goal of our group was/is not to take female members away from <a href="http://www.phillylinux.org">PLUG</a>.<br><br>

As has been pointed out, we intend to be more of a "recruiting force." Both of us either have (or know of women who have) been, in a situation where we felt too intimidated to speak up (what is motd, for e.g). We know  women who don't come to PLUG meetings for this reason, i.e a general apprehension/fear that all women in a male dominated field have experienced to some degree. It is sometimes difficult when a woman shows up at a meeting and ends up being the only female. It doesn't matter what your feelings are on gender issues, this <u>does</u> tend to make some women uncomfortable. It is also difficult for some women to feel comfortable joining a "tech group" because of this gender imbalance. We just wanted to provide a forum where women might feel more comfortable to express themselves. We will always encourage women in our group to join and become active in other local groups as well. <br><br>

A goal we do have is to bring female linux users together, for exposure to each other that we do not currently get. Many of us do not know other local women who use linux, and we really lack a local social net of fellow "geek girls." We wish to change that, and no longer feel like "I'm the only one."  We feel that having a female only environment for discussion that we might be able to boost confidence, and become more aware of the existence of other female linux users out there.<br><br>

What we don't want is for this to become a "PLUG 2"... that certainly would have the potential to fragment PLUG. But, we would like Philly-Chix to be a sister organization to PLUG in the same way <a href="http://www.cjfearnley.com/pads/">PADS</a> is.  We do not want guys showing up to our meetings trying to impress us, or find dates, or any of the typical things that many of us run into on a daily basis. Sometimes even the presence of men in a meeting can make us a bit shy and afraid to share our thoughts.<br><br>

Are there genuinely nice men out there who would probably make a suitable contribution to our group? Yes, but then comes the question of where we draw the line. We start making exceptions, things start getting complicated, arguments start, misunderstandings...etc. We don't want to have to deal with that sort of thing, it's bad for any group. So we decided to keep our list and regular meetings open to women only.<br><br>

It is true that some Linux Chix groups do let men join. But it mentioned in their FAQ (http://www.linuxchix.org/content/docs/faqs/) that it is up to the chapters to decide a course to take when considering this aspect. Because PLUG is such a large and active group we know that we are not the "only linux group in the area people can go to" so we assumed that this would not be an issue.<br><br>

P.S We have every intention of also having "events" where both men and women can participate (the site has been updated to reflect this)... It is by no means some crazy man-hating group of feminists. We are just women who want to spend time together and talk about geeky things :)<br><Br>

- Elizabeth Krumbach and Samantha Ollinger, Coordinators of the Philly Chix and still members of PLUG<br><br>
