<h3>Girls Inc Event</h3>
Saturday June 9, 2007<br><br>
</center>
Groups involved:
<ol>
<li><a href="http://www.girlsincpa-nj.org/">Girls Inc of Greater Philadelphia and Southern New Jersey</a></li>
<li><a href="http://www.mtairylearningtree.org/">MALT CVG (Mt Airy Learning Tree Computer Volunteer Group)</a> (<a href="http://mcvg.blogspot.com/">blog</a>)</li>
<li><a href="http://www.meetlinux.com/">Pennsylvania Ubuntu Community/Local Team</a></li>
<li><a href="http://www.phillychix.org/">Philadelphia LinuxChix Chapter</a></li>
</ol>

<i><b>Project:</b></i> 10 computers were donated to Girls Inc by the <a href="http://www.phlx.com/">Philadelphia Stock Exchange</a>. MALT and the PA Ubuntu team became involved. The PhillyChix were contacted indirectly through Lyz Bevilacqua's involvement with the <a href="http://ubuntu-women.org/">Ubuntu-Women Project</a> (local Ubuntu-Women event requests for Philadelphia are forward to the PhillyChix). It was worked out between the groups that the appropriate way to move forward with installing software on these machines was to create a dual boot system using Windows 2000 (MALT is a licensed Microsoft reseller) and Ubuntu with a shared fat32 data partition.<br><br>

<i><b>Execution:</b></i> Kevin Valentine of the Ubuntu PA group worked closely with MALT and Girls Inc to develop a plan and move forward on it. He spent several days with these groups setting up one system and then copying the disk image over to several of the other machines. On June 9th all the groups met at the Girls Inc location in Philadelphia. The Ubuntu PA team and PhillyChix set up the room, brought in monitors and helped bring up the machines when MALT arrived. Kevin and Lyz worked on imaging the remaining 4 machines and Lyz gave a brief talk to the entire group about Linux, Ubuntu and her own career in technology. MALT showed the girls around Windows, and before a lunch provided to volunteers by Girls Inc a few of the girls where shown around Ubuntu.<br><br>

<i><b>Conclusion:</b></i> There were time and other restraints placed on the volunteers. In the future a better planning schedule would be useful, as well as more time to show more of the girls their Ubuntu systems. We intend to hold a follow-up session for any of the girls who would like to return and have more training on their computers.<br><br>

<i><b>Thank You Letter:</b></i> The Girls Inc Organization sent out a Thank You Letter for all groups involved, you can view a copy of the letter <a href="http://phillychix.org/images/girls_inc_june_20_thankyou_letter.jpg">here</a>!<br><br>

